# ---------------------------------------------
# 任意の文字列から特定の文字列を出力する
# ---------------
value_str = 'abcde';

print(value_str[1]);
# 出力：b
print(value_str[-1]);
# 出力：e
print(value_str[0:3]);
# 出力：abc
print(value_str[1:3]);
# 出力：bc
print(value_str[1:]);
# 出力：bcde
print(value_str[:3]);
# 出力：abc


